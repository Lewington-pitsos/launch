function filterReleases(array) {
  return array.filter(hasIdTitle);
}

function hasIdTitle(obj) {
  return obj['title'] && (obj['id'] || obj['id'] === 0);
}

function justIdTitle(obj) {
  var result = {};
  result['title'] = obj['title'];
  result['id'] = obj['id'];
  return result;
}

function processReleaseData(array) {
  return filterReleases(array).map(justIdTitle);
}


